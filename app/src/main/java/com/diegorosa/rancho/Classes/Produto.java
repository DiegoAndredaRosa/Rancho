package com.diegorosa.rancho.Classes;

public class Produto {
    private String keyProduto;
    private String nomeProduto;
    private String quantMedidaProduto;
    private String precoUnitProduto;

    public String getKeyProduto() {
        return keyProduto;
    }

    public void setKeyProduto(String keyProduto) {
        this.keyProduto = keyProduto;
    }

    public String getNomeProduto() {
        return nomeProduto;
    }

    public void setNomeProduto(String nomeProduto) {
        this.nomeProduto = nomeProduto;
    }

    public String getQuantMedidaProduto() {
        return quantMedidaProduto;
    }

    public void setQuantMedidaProduto(String quantMedidaProduto) {
        this.quantMedidaProduto = quantMedidaProduto;
    }

    public String getPrecoUnitProduto() {
        return precoUnitProduto;
    }

    public void setPrecoUnitProduto(String precoUnitProduto) {
        this.precoUnitProduto = precoUnitProduto;
    }

}
